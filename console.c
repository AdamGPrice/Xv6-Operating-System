void consoleclearscreen(int colour) {
    if (currentvgamode == 0x13) {
        memset(VGA_0x13_MEMORY, colour, VGA_0x13_WIDTH * VGA_0x13_HEIGHT);
    } 
    else if (currentvgamode == 0x12) {
        int size = (VGA_0x12_WIDTH * VGA_0x12_HEIGHT) / 8;
        for (int i = 0; i < 4; i++) {
            int bit = (colour >> i) & 1;
            consolevgaplane(i);
            uchar* addr = (uchar*)consolevgabuffer();
            if (bit) {
                memset(addr, 255, size);
            } else {
                memset(addr, 0, size);
            }
        }
    }
}

int consolesetpixel(int x, int y, int colour) {
    if (currentvgamode == 0x13) {
        if (x < 0 || x >= VGA_0x13_WIDTH || y < 0 || y >= VGA_0x13_HEIGHT) {
            return -1;
        } else {
            uchar* addr = VGA_0x13_MEMORY + VGA_0x13_WIDTH * y + x;
            *addr = colour;
        }
    } 
    else if (currentvgamode == 0x12) {
        if (x < 0 || x >= VGA_0x12_WIDTH || y < 0 || y >= VGA_0x12_HEIGHT) {
            return -1;
        } else {
            int memoffset = VGA_0x12_WIDTH * y + x;
            int bitoffset = memoffset % 8;
            memoffset /= 8;

            for (int i = 0; i < 4; i++) {
                int bit = (colour >> i) & 1;
                consolevgaplane(i);
                uchar* addr = consolevgabuffer() + memoffset;
                uchar new = 128 >> bitoffset;
                if (bit) {
                    *addr = new | *addr;
                } else {
                    *addr = ~(new) & *addr;
                }
            }
        }
    }
    return 0;
}

void setpixelmode12(int x, int y, uchar byte, int colour) {
    
    int memoffset = VGA_0x12_WIDTH * y + x;
    memoffset /= 8;

    for (int i = 0; i < 4; i++) {
        int bit = (colour >> i) & 1;
        consolevgaplane(i);
        uchar* addr = consolevgabuffer() + memoffset;
        if (bit) {
            *addr = byte | *addr;
        } else {
            *addr = ~(byte) & *addr;
        }
    }
}

// Bresenham Line Algorithm
void consoledrawline(int x0, int y0, int x1, int y1, int colour) {
    // Need to make sure drawing from left to right for mode 12 algorithm to work
    if (x0 > x1) {
        int temp = x0;
        x0 = x1;
        x1 = temp;
        temp = y0;
        y0 = y1;
        y1 = temp;
    }

    int dx =  abs (x1 - x0); 
    int dy = -abs (y1 - y0);
    int sx = 1;
    int sy = y0 < y1 ? 1 : -1; 
    int err = dx + dy; 
    int e2;

    if (currentvgamode == 0x13) {
        consolesetpixel(x0, y0, colour);
        while (!(x0 == x1 && y0 == y1)) {

            e2 = 2 * err;
            if (e2 >= dy) { 
                err += dy; 
                x0 += sx; 
            }
            if (e2 <= dx) { 
                err += dx; 
                y0 += sy; 
            }
            consolesetpixel(x0, y0, colour);
        }
    } else if (currentvgamode == 0x12) {
        // slightly more efficient solution for draw line in mode 12
        // chunks the pixels together in the byte value
        int x = x0;
        int y = y0;
        int bitoffset = x0 % 8;
        uchar byte = 0;

        byte |= 128 >> bitoffset;

        while (!(x0 == x1 && y0 == y1)) {
            e2 = 2 * err;
            if (e2 >= dy) { 
                err += dy; 
                x0 += sx; 
            }
            if (e2 <= dx) { 
                err += dx; 
                y0 += sy; 
            }

            bitoffset = x0 % 8;
            if (bitoffset == 0 || y0 != y) {
                setpixelmode12(x, y, byte, colour);
                y = y0;
                x = x0;
                byte = 0;
            }

            byte |= 128 >> bitoffset;
        }
        // last line of pixels
        bitoffset = x0 % 8;
        byte |= 128 >> bitoffset;

        setpixelmode12(x, y, byte, colour);
    }   
}

// Bresenham cicrlce algorithm
void circlepoints(int xc, int yc, int x, int y, int colour) {
    consolesetpixel(xc+x, yc+y, colour); 
    consolesetpixel(xc-x, yc+y, colour); 
    consolesetpixel(xc+x, yc-y, colour); 
    consolesetpixel(xc-x, yc-y, colour); 
    consolesetpixel(xc+y, yc+x, colour); 
    consolesetpixel(xc-y, yc+x, colour); 
    consolesetpixel(xc+y, yc-x, colour); 
    consolesetpixel(xc-y, yc-x, colour); 
}

void consoledrawcircle(int xc, int yc, int r, int colour) {
    int x = 0;
    int y = r; 
    int d = 3 - 2 * r; 
    circlepoints(xc, yc, x, y, colour); 
    while (y >= x) {       
        x++; 
        if (d > 0) { 
            y--;  
            d = d + 4 * (x - y) + 10; 
        } 
        else
            d = d + 4 * x + 6; 
        circlepoints(xc, yc, x, y, colour); 
    } 
}

void memsethorizontalline(int y, int x0, int x1, int colour) {
    int memoffset1 = VGA_0x12_WIDTH * y + x0;
    int bitoffset1 = memoffset1 % 8;
    memoffset1 /= 8;

    int memoffset2 = VGA_0x12_WIDTH * y + x1;
    int bitoffset2 = memoffset2 % 8;
    memoffset2 /= 8;

    for (int i = 0; i < 4; i++) {
        int bit = (colour >> i) & 1;
        consolevgaplane(i);
        uchar* memstart = consolevgabuffer() + memoffset1;

        if (bit) {
            memset(memstart + 1, 255, memoffset2 - memoffset1 - 1);
        }
        else {
            memset(memstart + 1, 0, memoffset2 - memoffset1 - 1);
        }
    }

    // do this for the sides of the rect where the pixels don't fit inside a full byte
    uchar left = 0;
    for (int i = bitoffset1; i < 8; i++) {
        left |= 128 >> i;
    }
    setpixelmode12(x0, y, left, colour);
    uchar right = 0;
    for (int i = 0; i < bitoffset2; i++) {
        right |= 128 >> i;
    }
    setpixelmode12(x1, y, right, colour);
}

void consolefillrect(int x, int y, int width, int height, int colour) {
    // if width is 1 byte or less just use simple algorithm for mode 12
    if (currentvgamode == 0x13 ||  width <= 8) {
        for (int i = x; i < x + width; i++) {
            for (int j = y; j < y + height; j++) {
                consolesetpixel(i, j, colour);
            }
        }
    } else if (currentvgamode == 0x12) {
        // little bit of error handling
        if (x + width > VGA_0x12_WIDTH) {
            width = VGA_0x12_WIDTH - x;
        }
        if (y + height > VGA_0x12_HEIGHT) {
            height = VGA_0x12_HEIGHT - y;
        }

        // More efficienct fill rect for mode 12
        // memeset each line of the rect with a few exceptions
        for (int i = y; i < y + height; i++) {
            memsethorizontalline(i, x, x + width, colour);
        }
    }
}

typedef struct edgebucket {
    int ymax;
    int ymin;
    int x;
    int sign;
    int dx;
    int dy;
    int sum;
} edgebucket;

typedef struct edgelist {
    edgebucket array[20];
    int count;
} edgelist;

int add(edgelist *edges, edgebucket edge) {
    if (edge.ymax != edge.ymin) {
        edges->array[edges->count] = edge;
        edges->count++;
        return 0;
    } else {
        return -1;
    }
}

edgebucket remove(edgelist *edges, int index) {
    edgebucket removed = edges->array[index];
    for (int i = index; i < edges->count - 1; i++) {
        edges->array[i] = edges->array[i + 1];
    }
    edges->count--;
    return removed;
}

edgebucket pairvertices(int x0, int y0, int x1, int y1) {
    int ymax;
    int ymin;
    int x;
    int sign = 0;
    if (y0 > y1) {
        ymax = y0;
        ymin = y1;
        x = x0;
        if (x0 != x1) {
            sign = x0 < x1 ? 1 : -1;
        }
    } else {
        ymax = y1;
        ymin = y0;
        x = x1;
        if (x0 != x1) {
            sign = x1 < x0 ? 1 : -1;
        }
    }

    edgebucket edge = (struct edgebucket){ .ymax = ymax, .ymin = ymin, .x = x, .sign = sign,
        .dx = abs (x1 - x0), .dy = abs (y1 - y0), .sum = 0 };
    return edge;
}

void sortymax(edgelist *edges) {
    // bubble sort :)
    bool sorted = 0;
    while (!sorted) {
        sorted = 1;
        for (int i = 0; i < edges->count - 1; i++) {
            if (edges->array[i].ymax < edges->array[i + 1].ymax) {
                sorted = 0;
                edgebucket temp = edges->array[i];
                edges->array[i] = edges->array[i + 1];
                edges->array[i + 1] = temp;
            }
        }
    }
}

void sortx(edgelist *edges) {
    // bubble sort :)
    bool sorted = 0;
    while (!sorted) {
        sorted = 1;
        for (int i = 0; i < edges->count - 1; i++) {
            if (edges->array[i].x > edges->array[i + 1].x) {
                sorted = 0;
                edgebucket temp = edges->array[i];
                edges->array[i] = edges->array[i + 1];
                edges->array[i + 1] = temp;
            }
        }
    }
}

